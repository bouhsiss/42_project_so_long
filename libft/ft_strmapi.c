#include"libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*str;
	int		len;
	int		i;

	len = 0;
	i = 0;
	if (!(s))
		return (NULL);
	while (s[len])
		len++;
	str = (char *)malloc(sizeof (char) * len + 1);
	if (!(str))
		return (NULL);
	while (s[i])
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
